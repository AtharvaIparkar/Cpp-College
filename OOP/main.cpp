//ADDITION OF TWO NOS.
#include<iostream>
using namespace std;
int main()
{
int a,b,add;
cout <<"Enter two no. : ";
cin >> a >> b;
add = a + b;
cout <<"Addition is : " << add;
return 0;
}

//AREA OF CIRCLE
# include <iostream>
#define pi 3.14
using namespace std;
int main()
{
float r,multi;
cout << "Enter radius of circle : ";
cin >> r;
multi = pi*r*r;
cout << "Area of circle is : " << multi;
return 0;
}

